package CondicionaisEncadeadas;

public class CondicionaisEncadeadas {
    public static void main(String[] args) {
        int nota = 65;

        if (nota >= 90) {
            System.out.println("Aprovado");
        } else if (nota >= 80) {
            System.out.println("Aprovado");
        } else if (nota >= 70) {
            System.out.println("Aprovado");
        } else if (nota >= 60) {
            System.out.println("Reprovado");
        } else {
            System.out.println("Reprovado");
        }
    }
}
